#include <stdio.h>

int main(){
    int a,m1000,m500,m100,m50,m20,m10,m1;
    scanf("%d",&a);
    m1000 = a/1000;
    m500 = (a-(m1000*1000))/500;
    m100 = (a-(m1000*m1000)-(m500*500))/100;
    m50 = (a-(m1000*m1000)-(m500*500)-(m100*m100))/50;
    m20 = (a-(m1000*m1000)-(m500*500)-(m100*m100)-(m50*m50))/20;
    m10 = (a-(m1000*m1000)-(m500*500)-(m100*m100)-(m50*m50)-(m20*m20))/10;
    m1 = (a-(m1000*m1000)-(m500*500)-(m100*m100)-(m50*m50)-(m20*m20)-(m10*m10))/1;
    printf("%d\n%d\n%d\n%d\n%d\n%d\n%d",m1000,m500,m100,m50,m20,m10,m1);
    return 0;
}